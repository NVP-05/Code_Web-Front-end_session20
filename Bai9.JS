let a = 5;
let b = "5";
console.log(a==b);  // == thì chỉ quan tâm đến giá trị xem hai phần tử so sánh đó có bằng nhau không
console.log(a===b); //còn === thì "nghiêm ngặt" hơn ngoài so sánh giá trị n còn quan tâm đến cả kiểu dữ liệu của các phần tử được so sánh
console.log(a!=b);  //tương tự ==
console.log(a!==b); //tương tự ===